{
  /* USER CODE BEGIN Callback 0 */

  /* USER CODE END Callback 0 */
  if (htim->Instance == TIM11) {
    HAL_IncTick();
  }
  /* USER CODE BEGIN Callback 1 */
  if (htim->Instance == TIM10) { // add PSJ 0930
      TIM10_1ms_counter++;
      TIM10_1ms_counter1++;
      TIM10_DHT11_counter++;
      TIM10_servomotor_counter++;
   }
  /* USER CODE END Callback 1 */
}

